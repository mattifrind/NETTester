package datasettester.parsing;

/**
 * Coordinates used in Triangulation.java
 * @author HTWK Leipzig
 */
class Coord {
    
    double x;
    double y;
    double z;

    public Coord(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public Coord(double x, double y, double z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
    
}
